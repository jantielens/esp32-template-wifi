// ============================================================================
// ST7703_DSI_Driver — subclass overrides
// ============================================================================

const mipi_dsi_init_cmd_t* ST7703_DSI_Driver::getInitCommands() const {
    return st7703_init_operations;
}

size_t ST7703_DSI_Driver::getInitCommandCount() const {
    return sizeof(st7703_init_operations) / sizeof(st7703_init_operations[0]);
}

const char* ST7703_DSI_Driver::getLogTag() const {
    return "ST7703";
}

MipiDsiTimingConfig ST7703_DSI_Driver::getTimingConfig() const {
    return {
        .dpi_clock_hz = ST7703_DPI_CLK_HZ,
        .lane_bit_rate_mbps = ST7703_LANE_BIT_RATE,
        .hsync_pulse_width = ST7703_HSYNC_PULSE_WIDTH,
        .hsync_back_porch = ST7703_HSYNC_BACK_PORCH,
        .hsync_front_porch = ST7703_HSYNC_FRONT_PORCH,
        .vsync_pulse_width = ST7703_VSYNC_PULSE_WIDTH,
        .vsync_back_porch = ST7703_VSYNC_BACK_PORCH,
        .vsync_front_porch = ST7703_VSYNC_FRONT_PORCH,
        .disable_lp = true,  // ST7703 flickers cyan without continuous HS mode
    };
}
